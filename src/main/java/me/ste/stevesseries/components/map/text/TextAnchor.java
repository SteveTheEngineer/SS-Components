package me.ste.stevesseries.components.map.text;

/**
 * The position at which the text will be rendered
 */
public enum TextAnchor {
    /**
     * The start position of the text. For vertical it's the top, and for horizontal it's the left
     */
    START,

    /**
     * The middle position of the text. It's the center for both horizontal and vertical
     */
    MIDDLE,

    /**
     * The end position of the text. For vertical it's the bottom, and for horizontal it's the right
     */
    END
}